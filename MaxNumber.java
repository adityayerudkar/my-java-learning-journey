public class MaxNumber {
    public static void main(String[] args) {
        int a = 10, b = 20;

        System.out.println("Numbers are: " + a + " and " + b);

        if (a > b) {
            System.out.println(a + " is larger");
        } else {
            System.out.println(b + " is larger");
        }
    }
}
